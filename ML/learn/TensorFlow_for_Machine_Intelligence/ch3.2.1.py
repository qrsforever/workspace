import tensorflow as tf


#####################################################################################
# <codecell> 定义数据流
#####################################################################################
a = tf.constant(5, name='input_a')
b = tf.constant(3, name='input_b')
c = tf.multiply(a, b, name='mul_c')
d = tf.add(a, b, name='add_d')
e = tf.add(c, d, name='add_e')

#####################################################################################
# <codecell> 运行数据流
#####################################################################################
sess = tf.Session()
sess.run(e)

#####################################################################################
# <codecell> 
#####################################################################################
writer = tf.summary.FileWriter('/tmp/tf', sess.graph)


#####################################################################################
# <codecell> 关闭资源
#####################################################################################
writer.close()
sess.close()
